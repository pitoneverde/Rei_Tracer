#include "ft_printf.h"

static int	ft_ismodi(int c)
{
	if (c == '-' || c == '0' || c == '.' || c == '*'
		|| c == '#' || c == ' ' || c == '+')
		return (1);
	return (0);
}

int	ft_isflag(int c)
{
	return (ft_ismodi(c) || ft_isspec(c) || ft_isdigit(c));
}
