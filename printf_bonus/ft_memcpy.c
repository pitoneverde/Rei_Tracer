#include "ft_printf.h"
#include <stddef.h>

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	const char	*s;
	char		*d;

	s = s2;
	d = s1;
	while (n-- > 0)
		*d++ = *s++;
	return (s1);
}
